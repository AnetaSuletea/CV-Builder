    private void AddDigitalCompetenceActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddDigitalCompetenceActionPerformed

        DCInformationProcessing.addElement(DCInformationProcessingLevel.getSelectedItem());
        if(DCInformationProcessingFlag!=true){
            JOptionPane.showMessageDialog(this,"Please select the level of Information processing aptitudes");
        }
        else{
            DCCommunication.addElement(DCCommunicationLevel.getSelectedItem());
            if(DCCommunicationFlag!=true){
                JOptionPane.showMessageDialog(this,"Please select the level of communication aptitudes");
            }
            else{
                DCContentCreation.addElement(DCContentCreationLevel.getSelectedItem());
                if(DCContentCreationFlag!=true){
                    JOptionPane.showMessageDialog(this,"Please select the level of content creation aptitudes");
                }
                else{
                    DCSafety.addElement(DCSafetyLevel.getSelectedItem());
                    if(DCSafetyFlag!=true){
                        JOptionPane.showMessageDialog(this,"Please select the level of safety aptitudes");
                    }
                    else{
                        DCProblemSolving.addElement(DCProblemSolvingLevel.getSelectedItem());
                        if(DCProblemSolvingFlag!=true){
                            JOptionPane.showMessageDialog(this,"Please select the level of problem solving aptitudes");
                        }
                        else{
                            DCCertificate.addElement(DigitalCertificateField.getText());
                            if(((String)DCCertificate.getElementAt(NumberOfDC)).length()<5 || DCCertificateFlag!=true){
                                JOptionPane.showMessageDialog(this,"Please enter a valid certificate name");
                                DCCertificate.removeElementAt(NumberOfDC);
                            }
                            else{
                                NumberOfDC++;
                                JOptionPane.showMessageDialog(this,"Digital Certificate successfully added");
                                if(NumberOfDC>0){
                                    DigitalCertificateField.setText((String)DCCertificate.getElementAt(NumberOfDC-1));
                                    DigitalCertificateField.setForeground(new java.awt.Color(204, 204, 204));
                                }
                                else{
                                    DigitalCertificateField.setForeground(new java.awt.Color(204, 204, 204));
                                }
                                DCInformationProcessingLevel.setSelectedItem("Basic User");
                                DCInformationProcessingFlag=false;
                                DCCommunicationLevel.setSelectedItem("Basic User");
                                DCCommunicationFlag=false;
                                DCContentCreationLevel.setSelectedItem("Basic User");
                                DCContentCreationFlag=false;
                                DCSafetyLevel.setSelectedItem("Basic User");
                                DCSafetyFlag=false;
                                DCProblemSolvingLevel.setSelectedItem("Basic User");
                                DCProblemSolvingFlag=false;

                            }
                        }
                    }
                }
            }

        }
    }//GEN-LAST:event_AddDigitalCompetenceActionPerformed

