    private void AddEducationActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddEducationActionPerformed
        StartEducationDate.addElement(StartEducationDateField.getText());
        //StartWorkDate.getElementAt(NumberOfJobs);
        if(((String)StartEducationDate.getElementAt(NumberOfStudies)).length()!=10 || StartEducationDateFlag!=true){
            JOptionPane.showMessageDialog(this,"Please enter a valid education start date");
            StartEducationDate.removeElementAt(NumberOfStudies);
        }
        else{
            EndEducationDate.addElement(EndEducationDateField.getText());
            if((((String)EndEducationDate.getElementAt(NumberOfStudies)).length()!=10 || EndEducationDateFlag!=true)&& !StudiesOngoing.isSelected()){
                JOptionPane.showMessageDialog(this,"Please enter a valid education end date");
                StartEducationDate.removeElementAt(NumberOfStudies);
                EndEducationDate.removeElementAt(NumberOfStudies);
            }

            else{
                Qualification.addElement(QualificationField.getText());
                if(((String)Qualification.getElementAt(NumberOfStudies)).length()<3 || QualificationFlag!=true){
                    JOptionPane.showMessageDialog(this,"Please enter a valid qualification title");
                    StartEducationDate.removeElementAt(NumberOfStudies);
                    EndEducationDate.removeElementAt(NumberOfStudies);
                    Qualification.removeElementAt(NumberOfStudies);
                }
                else{
                    OrganisationName.addElement(OrganisationNameField.getText());
                    if(((String)OrganisationName.getElementAt(NumberOfStudies)).length()<3|| OrganisationNameFlag!=true){
                        JOptionPane.showMessageDialog(this,"Please enter a valid organisation's name");
                        StartEducationDate.removeElementAt(NumberOfStudies);
                        EndEducationDate.removeElementAt(NumberOfStudies);
                        Qualification.removeElementAt(NumberOfStudies);
                        OrganisationName.removeElementAt(NumberOfStudies);
                    }
                    else{
                        OrganisationCity.addElement(OrganisationCityField.getText());
                        if(((String)OrganisationCity.getElementAt(NumberOfStudies)).length()<3|| OrganisationCityFlag!=true){
                            JOptionPane.showMessageDialog(this,"Please enter a valid organisation's city");
                            StartEducationDate.removeElementAt(NumberOfStudies);
                            EndEducationDate.removeElementAt(NumberOfStudies);
                            Qualification.removeElementAt(NumberOfStudies);
                            OrganisationName.removeElementAt(NumberOfStudies);
                            OrganisationCity.removeElementAt(NumberOfStudies);
                        }
                        else{
                            OrganisationCountry.addElement(OrganisationCountryField.getText());
                            if(((String)OrganisationCountry.getElementAt(NumberOfStudies)).length()<3|| OrganisationCountryFlag!=true){
                                JOptionPane.showMessageDialog(this,"Please enter a valid organisation's country");
                                StartEducationDate.removeElementAt(NumberOfStudies);
                                EndEducationDate.removeElementAt(NumberOfStudies);
                                Qualification.removeElementAt(NumberOfStudies);
                                OrganisationName.removeElementAt(NumberOfStudies);
                                OrganisationCity.removeElementAt(NumberOfStudies);
                                OrganisationCountry.removeElementAt(NumberOfStudies);
                            }
                            else{
                                MainSubjects.addElement(MainSubjectsField.getText());
                                if(((String)MainSubjects.getElementAt(NumberOfStudies)).length()<3|| MainSubjectsFlag!=true){
                                    JOptionPane.showMessageDialog(this,"Please enter valid main subjects");
                                    StartEducationDate.removeElementAt(NumberOfStudies);
                                    EndEducationDate.removeElementAt(NumberOfStudies);
                                    Qualification.removeElementAt(NumberOfStudies);
                                    OrganisationName.removeElementAt(NumberOfStudies);
                                    OrganisationCity.removeElementAt(NumberOfStudies);
                                    MainSubjects.removeElementAt(NumberOfStudies);
                                }
                                else{
                                    StartEducationDateField.setText((String)StartEducationDate.getElementAt(NumberOfStudies));
                                    StartEducationDateField.setForeground(new java.awt.Color(204, 204, 204));
                                    if(StudiesOngoing.isSelected()){
                                        EndEducationDate.removeElementAt(NumberOfStudies);
                                        EndEducationDate.addElement("Ongoing");
                                        StudiesOngoing.setSelected(false);
                                    }
                                    EndEducationDateField.setText((String)EndEducationDate.getElementAt(NumberOfStudies));
                                    EndEducationDateField.setForeground(new java.awt.Color(204, 204, 204));
                                    QualificationField.setText((String)Qualification.getElementAt(NumberOfStudies));
                                    QualificationField.setForeground(new java.awt.Color(204, 204, 204));
                                    OrganisationNameField.setText((String)OrganisationName.getElementAt(NumberOfStudies));
                                    OrganisationNameField.setForeground(new java.awt.Color(204, 204, 204));
                                    OrganisationCityField.setText((String)OrganisationCity.getElementAt(NumberOfStudies));
                                    OrganisationCityField.setForeground(new java.awt.Color(204, 204, 204));
                                    OrganisationCountryField.setText((String)OrganisationCountry.getElementAt(NumberOfStudies));
                                    OrganisationCountryField.setForeground(new java.awt.Color(204, 204, 204));
                                    MainSubjectsField.setText((String)MainSubjects.getElementAt(NumberOfStudies));
                                    MainSubjectsField.setForeground(new java.awt.Color(204, 204, 204));
                                    NumberOfStudies++;
                                    JOptionPane.showMessageDialog(this,"Education and training successfully added!");
                                }
                            }
                        }
                    }
                }
            }
        }
    }//GEN-LAST:event_AddEducationActionPerformed

