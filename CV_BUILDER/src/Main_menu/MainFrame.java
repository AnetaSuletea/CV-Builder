    private void AddWorkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddWorkActionPerformed
        StartWorkDate.addElement(StartWorkDateField.getText());
        //StartWorkDate.getElementAt(NumberOfJobs);
        if(((String)StartWorkDate.getElementAt(NumberOfJobs)).length()!=10 || StartWorkDateFlag!=true){
            JOptionPane.showMessageDialog(this,"Please enter a valid work start date");
            StartWorkDate.removeElementAt(NumberOfJobs);
        }
        else{
            EndWorkDate.addElement(EndWorkDateField.getText());
            if((((String)EndWorkDate.getElementAt(NumberOfJobs)).length()!=10 || EndWorkDateFlag!=true) && !WorkOngoing.isSelected()){
                JOptionPane.showMessageDialog(this,"Please enter a valid work end date");
                StartWorkDate.removeElementAt(NumberOfJobs);
                EndWorkDate.removeElementAt(NumberOfJobs);
            }
            else{
                PositionHeld.addElement(PositionHeldField.getText());
                if(((String)PositionHeld.getElementAt(NumberOfJobs)).length()<3 || PositionHeldFlag!=true){
                    JOptionPane.showMessageDialog(this,"Please enter a valid position");
                    StartWorkDate.removeElementAt(NumberOfJobs);
                    EndWorkDate.removeElementAt(NumberOfJobs);
                    PositionHeld.removeElementAt(NumberOfJobs);
                }
                else{
                    EmployerName.addElement(EmployerNameField.getText());
                    if(((String)EmployerName.getElementAt(NumberOfJobs)).length()<3|| EmployerNameFlag!=true){
                        JOptionPane.showMessageDialog(this,"Please enter a valid employer's name");
                        StartWorkDate.removeElementAt(NumberOfJobs);
                        EndWorkDate.removeElementAt(NumberOfJobs);
                        PositionHeld.removeElementAt(NumberOfJobs);
                        EmployerName.removeElementAt(NumberOfJobs);
                    }
                    else{
                        EmployerCity.addElement(EmployerCityField.getText());
                        if(((String)EmployerCity.getElementAt(NumberOfJobs)).length()<3|| EmployerCityFlag!=true){
                            JOptionPane.showMessageDialog(this,"Please enter a valid employer's city");
                            StartWorkDate.removeElementAt(NumberOfJobs);
                            EndWorkDate.removeElementAt(NumberOfJobs);
                            PositionHeld.removeElementAt(NumberOfJobs);
                            EmployerName.removeElementAt(NumberOfJobs);
                            EmployerCity.removeElementAt(NumberOfJobs);
                        }
                        else{
                            EmployerCountry.addElement(EmployerCountryField.getText());
                            if(((String)EmployerCountry.getElementAt(NumberOfJobs)).length()<3|| EmployerCountryFlag!=true){
                                JOptionPane.showMessageDialog(this,"Please enter a valid employer's country");
                                StartWorkDate.removeElementAt(NumberOfJobs);
                                EndWorkDate.removeElementAt(NumberOfJobs);
                                PositionHeld.removeElementAt(NumberOfJobs);
                                EmployerName.removeElementAt(NumberOfJobs);
                                EmployerCity.removeElementAt(NumberOfJobs);
                                EmployerCountry.removeElementAt(NumberOfJobs);
                            }
                            else{
                                MainActivities.addElement(MainActivitiesField.getText());
                                if(((String)MainActivities.getElementAt(NumberOfJobs)).length()<3|| MainActivitiesFlag!=true){
                                    JOptionPane.showMessageDialog(this,"Please enter valid main activities");
                                    StartWorkDate.removeElementAt(NumberOfJobs);
                                    EndWorkDate.removeElementAt(NumberOfJobs);
                                    PositionHeld.removeElementAt(NumberOfJobs);
                                    EmployerName.removeElementAt(NumberOfJobs);
                                    EmployerCity.removeElementAt(NumberOfJobs);
                                    EmployerCountry.removeElementAt(NumberOfJobs);
                                    MainActivities.removeElementAt(NumberOfJobs);
                                }
                                else{
                                    StartWorkDateField.setText((String)StartWorkDate.getElementAt(NumberOfJobs));
                                    StartWorkDateField.setForeground(new java.awt.Color(204, 204, 204));
                                    if(WorkOngoing.isSelected()){
                                        EndWorkDate.removeElementAt(NumberOfJobs);
                                        EndWorkDate.addElement("Ongoing");
                                        WorkOngoing.setSelected(false);
                                    }
                                    EndWorkDateField.setText((String)EndWorkDate.getElementAt(NumberOfJobs));
                                    EndWorkDateField.setForeground(new java.awt.Color(204, 204, 204));
                                    PositionHeldField.setText((String)PositionHeld.getElementAt(NumberOfJobs));
                                    PositionHeldField.setForeground(new java.awt.Color(204, 204, 204));
                                    EmployerNameField.setText((String)EmployerName.getElementAt(NumberOfJobs));
                                    EmployerNameField.setForeground(new java.awt.Color(204, 204, 204));
                                    EmployerCityField.setText((String)EmployerCity.getElementAt(NumberOfJobs));
                                    EmployerCityField.setForeground(new java.awt.Color(204, 204, 204));
                                    EmployerCountryField.setText((String)EmployerCountry.getElementAt(NumberOfJobs));
                                    EmployerCountryField.setForeground(new java.awt.Color(204, 204, 204));
                                    MainActivitiesField.setText((String)MainActivities.getElementAt(NumberOfJobs));
                                    MainActivitiesField.setForeground(new java.awt.Color(204, 204, 204));
                                    NumberOfJobs++;
                                    JOptionPane.showMessageDialog(this,"Work experience successfully added!");
                                }
                            }
                        }
                    }
                }
            }
        }
    }//GEN-LAST:event_AddWorkActionPerformed

