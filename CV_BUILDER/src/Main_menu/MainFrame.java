    private void AddPersonalInfoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddPersonalInfoActionPerformed
        if(AddPersonalInformationPressed==false){
            Fname=FirstNameField.getText();
            if(Fname.length()<3 || FirstNameFlag!=true){
                JOptionPane.showMessageDialog(this,"Please enter a valid First Name");
            }
            else{
                Lname=LastNameField.getText();
                if(Lname.length()<3 || LastNameFlag!=true){
                    JOptionPane.showMessageDialog(this,"Please enter a valid Last Name");
                }
                else{
                    DOB=DobField.getText();
                    if(DOB.length()!=10 || DOBFlag!=true){
                        JOptionPane.showMessageDialog(this,"Please enter a valid Date of birth");
                    }
                    else{
                        City=CityField.getText();
                        if(City.length()<3 || CityFlag!=true){
                            JOptionPane.showMessageDialog(this,"Please enter a valid City Name");
                        }
                        else{
                            Country=CountryField.getText();
                            if(City.length()<3 || CountryFlag!=true){
                                JOptionPane.showMessageDialog(this,"Please enter a valid Country Name");
                            }
                            else{
                                StreetAddress=StreetAddressField.getText();
                                if(StreetAddress.length()<5 || StreetAddressFlag!=true){
                                    JOptionPane.showMessageDialog(this,"Please enter a valid Street Address");
                                }
                                else{
                                    Telephone=TelField.getText();
                                    if(Telephone.length()<8 || TelephoneFlag!=true){
                                        JOptionPane.showMessageDialog(this,"Please enter a valid Telephone Number");
                                    }
                                    else{
                                        Email=EmailField.getText();
                                        if(Email.length()<7 || EmailFlag!=true){
                                            JOptionPane.showMessageDialog(this,"Please enter a valid E-mail");
                                        }
                                        else{
                                            if(Male.isSelected()){
                                                Sex="Male";
                                            }
                                            else if (Female.isSelected()){
                                                Sex="Female";
                                            }
                                            else if(DontIndicate.isSelected()){
                                                Sex=null;
                                            }
                                            else{
                                                JOptionPane.showMessageDialog(this,"Please specify your Sex");
                                            }
                                            JOptionPane.showMessageDialog(this,"Personal information successfully added!");
                                            FirstNameField.setForeground(new java.awt.Color(204, 204, 204));
                                            LastNameField.setForeground(new java.awt.Color(204,204,204));
                                            DobField.setForeground(new java.awt.Color(204,204,204));
                                            CityField.setForeground(new java.awt.Color(204,204,204));
                                            CountryField.setForeground(new java.awt.Color(204,204,204));
                                            StreetAddressField.setForeground(new java.awt.Color(204,204,204));
                                            TelField.setForeground(new java.awt.Color(204,204,204));
                                            EmailField.setForeground(new java.awt.Color(204,204,204));
                                            AddPersonalInformationPressed=true;

                                        }
                                    }
                                }
                            }
                        }
                    }
                }
            }
        }

    }//GEN-LAST:event_AddPersonalInfoActionPerformed

